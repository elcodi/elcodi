namespace Elcodi\Component\Product\Entity\Traits;

trait NameTrait
{
    /**
     * @var string
     *
     * Name
     */
    protected $name;

    /**
     * Set name
     *
     * @param string $name Name
     *
     * @return $this Self object
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string name
     */
    public function getName()
    {
        return $this->name;
    }

}
